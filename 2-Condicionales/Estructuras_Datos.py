#Listas
lista = ['Hola', 'Mundo', 123, True, 3.14]
print(lista) #imprime la lista
print(lista[2]) #accede a cualquier elemento de la lista
print(lista[:4]) #accede a los cuatros elementos, del 0 al 3
lista.append('este es otro elemento') #append es para agregar elementos
print(len(lista)) #muestra la longitud de la lista
lista.remove(lista[0]) #remove es para quitar elementos